## This file includes programs to declare variables of different data types like (integer, float, string, and boolean) and print each variable and its type.
# Declaring variables of different data types
a = 10
b = 10.5
c = "Hello, World!"
d = True
print("Integer:", a, "Type:", type(a))
print("Float:", b, "Type:", type(b))
print("String:", c, "Type:", type(c))
print("Boolean:", d, "Type:", type(d))